package internation.model;



public class Flag {

	private boolean fBasic;
	private boolean fStudyVideo;
	private boolean fReview;
	private boolean fHelp;
	
	public boolean isfHelp() {
		return fHelp;
	}

	public void setfHelp(boolean fHelp) {
		this.fHelp = fHelp;
	}

	public Flag()
	{
		
	}

	public boolean isfBasic() {
		return fBasic;
	}

	public void setfBasic(boolean fBasic) {
		this.fBasic = fBasic;
	}

	public boolean isfStudyVideo() {
		return fStudyVideo;
	}

	public void setfStudyVideo(boolean fStudyVideo) {
		this.fStudyVideo = fStudyVideo;
	}

	public boolean isfReview() {
		return fReview;
	}

	public void setfReview(boolean fReview) {
		this.fReview = fReview;
	}
	
	
	
}
